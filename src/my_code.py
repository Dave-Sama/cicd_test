def add_numbers(a, b):
    """
    Adds two numbers and returns the result.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The sum of the two numbers.
    """
    return a + b


if __name__ == "__main__":
    print("The sum of 3 and 5 is:", add_numbers(3, 5))
